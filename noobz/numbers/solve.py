def count_occurrences(target_digit, limit):
    count = 0
    for number in range(1, limit):
        current_number = number
        while current_number > 0:
            if current_number % 10 == target_digit:
                count += 1
            current_number //= 10
        
        if target_digit == 0 and number == 0:
            count += 1

    return count

